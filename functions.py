#including parameters in functions
#Parameter is additional piece of information 

def say_hello(name):
    print("Hello " + name)

say_hello("Mike")
say_hello("Singh")

def say_hello2(name, age):
    print("Hello " + name + " " + age)

say_hello2("Mike", "35")
say_hello2("Singh", "18")
